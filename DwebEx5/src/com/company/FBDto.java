package com.company;

public class FBDto {
	int idx;
	String fmember_id;
	String fmember_name;
	String title;
	String content;
	String regdate;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getFmember_id() {
		return fmember_id;
	}
	public void setFmember_id(String fmember_id) {
		this.fmember_id = fmember_id;
	}
	public String getFmember_name() {
		return fmember_name;
	}
	public void setFmember_name(String fmember_name) {
		this.fmember_name = fmember_name;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
}
